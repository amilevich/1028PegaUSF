package problemz15;

public class Solution15 implements SomeInterface{
	
	public int addition(int a, int b){
		return a+b; 
	}
	public int subtraction(int a, int b){
		return a-b; 
	}
	public int multiplication(int a, int b){
		return a*b; 
	}
	public float division(float a, float b){
		return a/b; 
	}
	
}
