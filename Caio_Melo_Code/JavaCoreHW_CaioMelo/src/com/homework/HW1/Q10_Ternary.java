/*
 * Q10 - Find minimum of two numbers using ternary operators
 * 
 * 
 */
public class Q10_Ternary {

	public static void main(String[] args) {
		
		
		int x = 6;
		int y = 4;
		
		int min = x>y ? y : x; //Ternary Operators are pretty cool.
		
		System.out.println(min);
		
	}
	
}
