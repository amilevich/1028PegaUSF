package com.javahomework.questions;

public class QuestionTenClass {
	/*
	 * Find the minimum of two numbers using ternary operators.
	 * */
	public static void main(String[] args) {
		int val1 = 10; // declaring my two variables
		int val2 = 20;

		int min = val1 <= val2 ? val1 : val2; // storing the condition's result in another variable (true or false)
		System.out.println(min);
	}
	
}
